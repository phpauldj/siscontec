package edu.upc.entidades;

/**
 *
 * @author Eduardo
 */
public class persona {
    private String usuario;
    private String clave;
    private String ruc;
    private String nombre;
    private String apellido;
    private String direccion;
    private String fecnaci;
    private String rpta;

    /**
     * @return the usuario
     */
    public String getUsuario() {
        return usuario;
    }

    /**
     * @param usuario the usuario to set
     */
    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    /**
     * @return the clave
     */
    public String getClave() {
        return clave;
    }

    /**
     * @param clave the clave to set
     */
    public void setClave(String clave) {
        this.clave = clave;
    }

    /**
     * @return the ruc
     */
    public String getRuc() {
        return ruc;
    }

    /**
     * @param ruc the ruc to set
     */
    public void setRuc(String ruc) {
        this.ruc = ruc;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the apellido
     */
    public String getApellido() {
        return apellido;
    }

    /**
     * @param apellido the apellido to set
     */
    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    /**
     * @return the direccion
     */
    public String getDireccion() {
        return direccion;
    }

    /**
     * @param direccion the direccion to set
     */
    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    /**
     * @return the fecnaci
     */
    public String getFecnaci() {
        return fecnaci;
    }

    /**
     * @param fecnaci the fecnaci to set
     */
    public void setFecnaci(String fecnaci) {
        this.fecnaci = fecnaci;
    }

    /**
     * @return the rpta
     */
    public String getRpta() {
        return rpta;
    }

    /**
     * @param rpta the rpta to set
     */
    public void setRpta(String rpta) {
        this.rpta = rpta;
    }
}
